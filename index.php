                    <h2 class="section-heading">About The Project </h2>
                    <p> Government of India’s proposal of the National Food Security Act (NFSA) aims to provide

basic requirement of food to its people. The vision, support and trust of the integral members

of the esteemed Ministry of Human Resources Department, Government of India were the

guiding light to initiate the project entitled “Sustainable Food Security through: Technical

Interventions for Production, Processing and Logistics” at IIT Kharagpur in January 2014.<br><br>

The implementation of NFSA is set to have a profound impact on India’s food supply chain

from production till delivery to the identified beneficiaries. The successful execution of

NFSA requires revolutionary changes across crop production, procurement, storage and

distribution, seamlessly coupled with information and communication technology (ICT).

Increasing food grain production has become a challenge because of diminishing per capita

arable land and availability of irrigation water resources and expanding abiotic and biotic

stresses under climate change/variability. At the same time, huge yield gap (&gt;50%) between

attainable and actual farmers yield in many parts of the country including eastern India offers

the scope for meeting the challenge. This can be possible through implementation of

scientific production technologies in farmers’ field. In this regard, Indian Institute of

Technology (IIT), Kharagpur has taken an initiative to demonstrate and increase awareness

about existing technologies on food grain production and post-harvest processing at Kenthia

village (Kharagpur Block–II, Paschim Medinipur), located within 10 km from the Institute

campus. The land was barren for last 5-7 years without any crop cultivation even in rainy

season. Lack of irrigation facility, farmers’ knowledge about cropping system management,

and rising labour cost are the major limitations of food production. Demonstration of

sustainable food production technologies has helped villagers not only to improve their

income by cultivating the barren land but also generated employment opportunities at village

level.    </p>
